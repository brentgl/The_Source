    <h2>Overview</h2>
    The Source is a customizable start page that provides quick access to tools, search engines and important websites. <br/>
    <h3><i class="far fa-bookmark"></i> Bookmarks</h3>
    Bookmarks can be managed by accessing the main directory or a category. <br/>
    The main directory can be accessed by clicking a letter on the letter bar. <br/>
    <h3><i class="far fa-star"></i> Favorite Section</h3>
    Favorites are bookmarks that have been selected in the directory to display in the Favorite section. <br/>
    The Favorite Section is the first area displayed on page load. <br/>
    <h3><i class="fas fa-th-list"></i> Category Section</h3>
    Bookmarks here are sorted by one of 9 different categories. <br/>
    Categories can be hidden in the Settings Menu. <br/>
    Categories are :
    <ul>
      <li>Design</li>
      <li>Environmental</li>
      <li>Finance</li>
      <li>Games</li>
      <li>Media</li>
      <li>Medical</li>
      <li>Network</li>
      <li>Productivity</li>
      <li>Web</li>
    </ul>
    <h2><i class="fas fa-tools"></i> Features</h2>
    <h3><i class="fab fa-google"></i> Google Search</h3>
    The Google Search bar is located on the Top Navigation Bar. <br/>
    Queries are sent to Google's search engine. <br/>
    The spyglass icon on the left of the bar will clear the input box. <br/>
    The spyglass on the right of the bar will launch the Multi-Search window. <br/>
    <h3><i class="fas fa-search-plus"></i> Multi-Search</h3>
    The Multi-Search window allows the user to search multiple sites from one interface. <br/>
    Queries are sent to search engine button that is click. <br/>
    The topmost part of the window contains pre-defined searches for various Google services. <br/>
    The bottom most part contains custom searches that can be configured in the settings menu. <br/>
    <h3><i class="fas fa-cloud-sun"></i> Weather</h3>
    The Weather Window will display a customizable weather forecast. <br/>
    The widget can be customized in the settings menu. <br/>
    <h3><i class="fas fa-unlock-alt"></i> Password Generator</h3>
    This window will generate a random password. <br/>
    The user specifies the length. <br/>
    Characters available: abcdefghijklmnopqrstuvwxyz0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ!#$%&*+-=?@_ <br/>
    <h3><i class="fas fa-envelope-open-text"></i> Custom Messages</h3>
    The Custom Messages is displayed on the Top Navigation Bar. <br/>
    These messages can be configured in the Settings Menu. <br/>
    <h3><i class="far fa-file-image"></i> Wallpaper</h3>
    Wallpaper can be customized in the Settings Menu. <br/>
    <h3><i class="fas fa-window-restore"></i> Frames</h3>
    The Source will display up to 5 customizable iframes. <br/>
    Content and dimensions of the iframes can be set up in the Settings Menu <br/>
    <h2><i class="fas fa-ellipsis-h"></i> Navigation</h2>
    <h3><i class="fas fa-list"></i> Main Directory</h3>
    The Main Directory will display all bookmarks. <br/>
    It can be accessed by clicking on one of the letters on the Letter Bar. <br/>
    A control bar will appear at the top of the screen. <br/>
    This bar will also appear when accessing a category. <br/>
    <h4><i class="far fa-star"></i> Favorite</h4>
    Toggles the "favorite" icon to be displayed. Allows the selection of favorite bookmarks.
    <h4><i class="far fa-trash-alt"></i> Remove</h4>
    Toggles the "Remove" icon to be displayed. Allows the removal of bookmarks. <br/>
    **Warning: Once removed, you will need to re-add the bookmark via the Add Bookmark window.
    <h4><i class="far fa-bookmark"></i> Add Bookmark</h4>
    Add a single bookmark to your collection
    <h4><i class="far fa-edit"></i> Edit Bookmarks</h4>
    Opens the MTE interface to Add/Remove/Change multiple bookmarks.
    <h4><i class="fas fa-list"></i> List View</h4>
    Toggles between Grid / List view
    <h3><i class="fas fa-ellipsis-v"></i> Side Menu</h3>
    The Side Menu is a quick menu that allows access to Favorites, Categories, Password Generator, Multi-Search, and your Custom Frames.
    <h3><i class="fas fa-reply fa-flip-horizontal"></i> Keyboard Shortcuts</h3>
    Displays the list of available Keyboard Shortcuts. <br/>
    <h3><i class="fas fa-user-cog" ></i> User Management</h3>
    Opens the HTAM (HTAccess Manager). <br/>
    Users and security settings can be managed here. <br/>
    <h2><i class="fas fa-cogs"></i> Settings</h2>
    <h3><i class="fas fa-vector-square"></i> General</h3>
    <h4><i class="fas fa-atlas"></i> Main URL</h4>
    Add your domain here (without the https:// or http://). <br/>
    This is used in different areas of the page <br/>
    <h4><i class="far fa-file-alt"></i> Page Title</h4>
    Displays in the tab of the page
    <h4><i class="fas fa-font"></i> Font Selection</h4>
    Select one of 5 different Google fonts for the page.
    <ul>
      <li>Comic Neue</li>
      <li>Open Sans</li>
      <li>Quicksand</li>
      <li>Roboto</li>
      <li>Source Sans Pro</li>
    </ul>
    <h3><i class="far fa-file-image"></i> Weather</h3>
    After configuring a 110px width weather widget from https://weatherwidget.io/ , paste the code here to update the Weather Window
    <h3><i class="fas fa-envelope-open-text"></i> Messages</h3>
    The message that appears in the Top Navigation Bar can be customized here. <br/>
    The message that is displayed is determined by the time of day. <br/>
    <h3><i class="fas fa-photo-video"></i> Media</h3>
    Wallpaper, Mobile Wallpaper, and Logo can be customized in the Media section of the Settings Menu.
    <h3><i class="fas fa-window-restore"></i> Custom Frames</h3>
    In the Custom Frames section of the Settings Menu, you can customize 5 different iframes. <br/>
    * Note: Some websites have restrictions that prevent their pages from displaying in an iframe. <br/>
    Customize Options :
    <ul>
      <li>Title</li>
      <li>URL</li>
      <li>Font Awesome Icon</li>
      <li>Window Dimensions</li>
    </ul>
    <h3><i class="fas fa-search-plus"></i> Custom Search</h3>
    In the Custom Search section of the Settings Menu, you can configure up to 24 different searches that will appear in the Multi-Search Window. <br/>
    * Note: To disable a search, clear the fields and type empty into the title. <br/>
    Customize Options :
    <ul>
      <li>Title</li>
      <li>URL</li>
      <li>Font Awesome Icon</li>
    </ul>
    <h3><i class="fas fa-th-list"></i> Categories</h3>
    In the Categories section of the Settings Menu, unused categories can be enabled/disabled
    <h3><i class="fas fa-tools"></i> Features</h3>
    In the Features section of the Settings Menu, unused features can be enabled/disabled
    <h3><i class="fab fa-google"></i> Google Search</h3>
    In the Google Searches section of the Settings Menu, unused Google Searches can be enabled/disabled.
    Customize Options :
    <ul>
      <li>Google Calendar</li>
      <li>Google Contacts</li>
      <li>Google Drive</li>
      <li>Google Earth</li>
      <li>Google Hangouts</li>
      <li>Google Keep</li>
      <li>Google Maps</li>
      <li>Google Photos</li>
      <li>Google Play</li>
      <li>Google Scholar</li>
      <li>Google Search</li>
      <li>Google Translate</li>
      <li>YouTube</li>
    </ul>
    <h3><i class="fab fa-css3-alt"></i> Custom CSS</h3>
    In the Custom CSS section of the Settings Menu, CSS for The Source can be overwritten with your own CSS. <br/>
